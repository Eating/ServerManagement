package eating.user;

public class UserInfo {
	private int id;
	private String loginName;
	private int type;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getUserName() {
		return loginName;
	}
	public void setUserName(String loginName) {
		this.loginName = loginName;
	}
	public int getType(){
		return type;
	}
	public void setType(int type){
		this.type = type;
	}
	
	

}
