package eating.user;

public class UserInfo {
	private int id;
	private String loginName;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getUserName() {
		return loginName;
	}
	public void setUserName(String loginName) {
		this.loginName = loginName;
	}
	
	

}
